#include <iostream>
using namespace std;

int main() {
	int a, b;
	cout << "Nhap vao hai so nguyen a va b:" << endl;
	cin >> a;
	cin >> b;
	cout << endl;
	cout << a << " + " << b << " = " << a + b << endl;
	cout << a << " - " << b << " = " << a - b << endl;
	cout << a << " * " << b << " = " << a * b << endl;
	cout << a << " / " << b << " = " << a / b << endl;
	return 0;
}